			<p><b>Comment fonctionne le forum ?</b> </p>
		 
				<p>La contrainte principale étant de ne poster qu’en images, l’envoi de texte ne sera autorisé que dans certains cas de figures (voir <a href="index.php?section=charte">la charte</a>). Par ailleurs, il n’y a pas de comptes sur la Commune Dessinée, cela pour favoriser la création de personnages plutôt que d’avatars. Les auteurs sont donc, de facto, invisibles, et il est interdit d’essayer d’émuler un système de compte en inscrivant - par exemple - un pseudonyme sur chacun de ses dessins.</p>				

				<p>Sur la Commune dessinée, on distingue deux types de sujet (ce qu'on appelle "topic" en bon globish) : d'une part les <b>sujets dessinés</b> sur lesquel le texte est formellement interdit sauf cas exceptionnel mentionné dans les premiers messages du sujet. Ce sont les sujets majoritaires. D'autre part, les <b>sujets écrits</b> qui sont minoritaires et servent principalement pour discuter (le format dessiné se prêtant mal au développement d'arguments précis). Il est recommandé de faire signer les messages écrits par l'un de vos personnages.</p>

			<p>Avant de participer à la Commune Dessinée, nous vous recommandons de lire <b><a href="index.php?section=charte">la charte</a></b>.</p>

