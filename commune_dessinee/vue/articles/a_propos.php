			<p><b>Qu’est-ce qu’un avatar ? un personnage ?</b> </p>
		

				<p>Sur la Commune Dessinée, vos personnages sont, dans l’idéal, dissociés de vous-même - l’auteur - à l’image des personnages de bandes dessinées, de romans ou de films. Ils ont une personnalité à part entière et ne vous représentent pas intégralement ; ce sont des outils de jeu avant tout. C’est ce qui les différencie des avatars, qui, eux, ont pour fonction de représenter l’auteur derrière les messages et de parler pour lui (puisqu’en dernière analyse, ils sont surtout sa version dessinée). Seuls les membres du <a href="index.php?section=politburo">Politburo</a> ont des avatars, afin que vous puissiez savoir lequel d’entre eux vous modère et à qui vous adresser en cas de problème.</p>

